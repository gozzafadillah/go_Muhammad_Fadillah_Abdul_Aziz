package calculator

func Adidition(a, b int) int {
	result := a + b
	return result
}

func Subtration(a, b int) int {
	result := a - b
	return result
}

func Division(a, b int) float64 {
	result := float64(a / b)
	return result
}

func Multiplication(a, b int) int {
	result := a * b
	return result
}
